import ch.fha.ia02.above.Application;

/**
 * Helper class that launches the <em>ABOVE</em> application
 * to make for a much nicer command line (and a nicer Application
 * menu on Mac OS X).
 *
 * @author Thomas Gerstendoerfer
 */
public class ABOVE
{
	/**
	 * Entry point to start the application.
	 * Just calls {@link Application#main(String[])}.
	 */
	public static void main(String[] args) {
		System.out.println("Starting ABOVE");
		Application.main(args);
	}
}
